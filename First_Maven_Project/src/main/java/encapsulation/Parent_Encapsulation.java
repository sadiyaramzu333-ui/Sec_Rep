package encapsulation;

public class Parent_Encapsulation {
	private String name;
	private int number;
	
	public void setName(String name)
	{
		this.name=name;
	}
	public String getName()
	{
		return name;
	}
	public void setAge(int number)
	{
		this.number=number;
	}
	public int getAge()
	{
		return number;
	}

}
